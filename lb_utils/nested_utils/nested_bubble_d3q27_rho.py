    rho = macrovars.rho
    u = macrovars.u
    v = macrovars.v
    w = macrovars.w
    c200 = cumulants.c200
    c020 = cumulants.c020
    c110 = cumulants.c110
    c002 = cumulants.c002
    c101 = cumulants.c101
    c011 = cumulants.c011
    inv_rho = 1.0/rho
    kappa002 = c002*rho
    kappa011 = c011*rho
    kappa020 = c020*rho
    kappa022 = (kappa002*kappa020 + 2*kappa011**2) * inv_rho
    kappa101 = c101*rho
    kappa110 = c110*rho
    kappa112 = (kappa002*kappa110 + 2*kappa011*kappa101) * inv_rho
    kappa121 = (2*kappa011*kappa110 + kappa020*kappa101) * inv_rho
    kappa200 = c200*rho
    kappa202 = (kappa002*kappa200 + 2*kappa101**2) * inv_rho
    kappa211 = (kappa011*kappa200 + 2*kappa101*kappa110) * inv_rho
    kappa220 = (kappa020*kappa200 + 2*kappa110**2) * inv_rho
    kappa222 = (-2*kappa002*kappa020*kappa200 - 4*kappa002*kappa110**2 - 4*kappa011**2*kappa200 - 16*kappa011*kappa101*kappa110 - 4*kappa020*kappa101**2 + rho*(kappa002*kappa220 + 4*kappa011*kappa211 + kappa020*kappa202 + kappa022*kappa200 + 4*kappa101*kappa121 + 4*kappa110*kappa112)) * inv_rho**2
    m_post_idx_0_0_c_0 = rho
    m_post_idx_0_0_c_1 = rho*w
    m_post_idx_0_0_c_2 = kappa002 + rho*w**2
    m_post_idx_0_1_c_1 = kappa011
    m_post_idx_0_1_c_2 = 2*kappa011*w
    m_post_idx_0_2_c_0 = kappa020
    m_post_idx_0_2_c_1 = kappa020*w
    m_post_idx_0_2_c_2 = kappa020*w**2 + kappa022
