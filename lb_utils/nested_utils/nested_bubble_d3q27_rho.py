@ti.func
def interpolation_FtoC(coeffs, omega):
    RC = coeffs.rho_coeffs
    UC = coeffs.u_coeffs
    VC = coeffs.v_coeffs
    WC = coeffs.w_coeffs
    INV_3omega = 1/(3*omega)
    UpScaling=2 # reconstructed gradient at F becomes twice when C observes it (chain rule in x_F <-> x_C)
    u = UC.C0
    v = VC.C0
    w = WC.C0
    rho = RC.C0
    c200 = -2*UC.Cx * INV_3omega * UpScaling + INV_3
    c020 = -2*VC.Cy * INV_3omega * UpScaling + INV_3
    c002 = -2*WC.Cz * INV_3omega * UpScaling + INV_3
    c110 = -(UC.Cy + VC.Cx) * INV_3omega * UpScaling
    c101 = -(UC.Cz + WC.Cx) * INV_3omega * UpScaling
    c011 = -(VC.Cz + WC.Cy) * INV_3omega * UpScaling
    macrovars = Macrovars(rho=rho, u=u, v=v, w=w)
    cumulants = Cumulants(c200=c200, c020=c020, c110=c110, c002=c002, c101=c101, c011=c011)
    return macrovars, cumulants

@ti.func
def interpolation_CtoF(coeffs, omega, coords):
    RC = coeffs.rho_coeffs
    UC = coeffs.u_coeffs
    VC = coeffs.v_coeffs
    WC = coeffs.w_coeffs
    x = coords[0]
    y = coords[1]
    z = coords[2]
    INV_3omega = 1/(3*omega)
    DownScaling=0.5 # reconstructed gradient at C becomes half  when F observes it (chain rule in x_C <-> x_F)
    u = UC.C0 + UC.Cx*x + UC.Cx2*x*x + UC.Cxy*x*y + UC.Cxyz*x*y*z + UC.Cxz*x*z + UC.Cy*y + UC.Cy2*y*y + UC.Cyz*y*z + UC.Cz*z + UC.Cz2*z*z
    v = VC.C0 + VC.Cx*x + VC.Cx2*x*x + VC.Cxy*x*y + VC.Cxyz*x*y*z + VC.Cxz*x*z + VC.Cy*y + VC.Cy2*y*y + VC.Cyz*y*z + VC.Cz*z + VC.Cz2*z*z
    w = WC.C0 + WC.Cx*x + WC.Cx2*x*x + WC.Cxy*x*y + WC.Cxyz*x*y*z + WC.Cxz*x*z + WC.Cy*y + WC.Cy2*y*y + WC.Cyz*y*z + WC.Cz*z + WC.Cz2*z*z
    rho = RC.C0 + RC.Cx*x + RC.Cx2*x*x + RC.Cxy*x*y + RC.Cxyz*x*y*z + RC.Cxz*x*z + RC.Cy*y + RC.Cy2*y*y + RC.Cyz*y*z + RC.Cz*z + RC.Cz2*z*z
    c200 = -2*(UC.Cx + 2*UC.Cx2*x + UC.Cxy*y + UC.Cxyz*y*z + UC.Cxz*z) * INV_3omega * DownScaling + INV_3
    c020 = -2*(VC.Cxy*x + VC.Cxyz*x*z + VC.Cy + 2*VC.Cy2*y + VC.Cyz*z) * INV_3omega * DownScaling + INV_3
    c002 = -2*(WC.Cxyz*x*y + WC.Cxz*x + WC.Cyz*y + WC.Cz + 2*WC.Cz2*z) * INV_3omega * DownScaling + INV_3
    c110 = -(UC.Cxy*x + UC.Cxyz*x*z + UC.Cy + 2*UC.Cy2*y + UC.Cyz*z + VC.Cx + 2*VC.Cx2*x + VC.Cxy*y + VC.Cxyz*y*z + VC.Cxz*z) * INV_3omega * DownScaling
    c101 = -(UC.Cxyz*x*y + UC.Cxz*x + UC.Cyz*y + UC.Cz + 2*UC.Cz2*z + WC.Cx + 2*WC.Cx2*x + WC.Cxy*y + WC.Cxyz*y*z + WC.Cxz*z) * INV_3omega * DownScaling
    c011 = -(VC.Cxyz*x*y + VC.Cxz*x + VC.Cyz*y + VC.Cz + 2*VC.Cz2*z + WC.Cxy*x + WC.Cxyz*x*z + WC.Cy + 2*WC.Cy2*y + WC.Cyz*z) * INV_3omega * DownScaling
    macrovars = Macrovars(rho=rho, u=u, v=v, w=w)
    cumulants = Cumulants(c200=c200, c020=c020, c110=c110, c002=c002, c101=c101, c011=c011)
    return macrovars, cumulants
